#!/usr/bin/env node

/**
 * Reads src/content/.obsidian/plugins/callout-manager/data.json and generates:
 *   src/styles/callouts-custom.css  — CSS color/border/bg vars per custom callout
 *   src/generated/callouts-custom.json — icon + title metadata for the remark plugin
 *
 * Color format:  Obsidian stores "r, g, b" bare RGB; we wrap to rgb(r, g, b).
 * Icon format:   Obsidian stores "lucide-flame" or emoji; we strip "lucide-" prefix.
 * Conditions:    { colorScheme: 'dark' } → .dark selector; theme/compound (and/or)
 *                conditions aren't scheme-specific, so they're flattened to apply
 *                to both light and dark (see classifyCondition).
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { join } from 'path';
import { pathToFileURL } from 'url';

const DATA_PATH    = join(process.cwd(), 'src/content/.obsidian/plugins/callout-manager/data.json');
const CSS_PATH     = join(process.cwd(), 'src/styles/callouts-custom.css');
const META_PATH    = join(process.cwd(), 'src/generated/callouts-custom.json');
const GENERATED_DIR = join(process.cwd(), 'src/generated');

const HEADER = `/* Auto-generated from src/content/.obsidian/plugins/callout-manager/data.json */
/* Do not edit manually — regenerated by: node scripts/generate-callout-css.js */\n\n`;

// Strip "lucide-" prefix; detect emoji (non-ASCII, no spaces = emoji or short symbol).
function normalizeIcon(raw) {
  if (!raw) return null;
  const s = raw.trim();
  if (s.startsWith('lucide-')) return { type: 'lucide', name: s.slice(7) };
  // Emoji: contains characters outside ASCII printable range
  if (/[^\x20-\x7E]/.test(s)) return { type: 'emoji', name: s };
  // Bare lucide name (no prefix, looks like kebab-case or single word)
  return { type: 'lucide', name: s };
}

// Derive a human-readable title from a callout ID.
function idToTitle(id) {
  return id.split(/[-_]/).map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
}

// Convert Obsidian color to a valid CSS color string.
// Obsidian uses bare "r, g, b" components; hex and rgb() strings pass through.
export function toColor(raw) {
  if (!raw) return null;
  const s = raw.trim();
  // Bare RGB components: "82, 144, 212"
  if (/^\d+\s*,\s*\d+\s*,\s*\d+$/.test(s)) {
    const [r, g, b] = s.split(',').map(v => v.trim());
    return `rgb(${r}, ${g}, ${b})`;
  }
  return s; // hex, rgb(), hsl(), etc. — already valid
}

// Extract RGB integers from a resolved CSS color for rgba() derivation.
function extractRGB(color) {
  const hex = color.match(/^#([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i);
  if (hex) return [parseInt(hex[1], 16), parseInt(hex[2], 16), parseInt(hex[3], 16)];
  const rgb = color.match(/^rgb\((\d+),\s*(\d+),\s*(\d+)\)$/);
  if (rgb) return [+rgb[1], +rgb[2], +rgb[3]];
  return null;
}

// Build --callout-border and --callout-bg from a resolved color.
function derivedVars(color, borderOpacity, bgOpacity) {
  const rgb = extractRGB(color);
  if (!rgb) return { border: color, bg: color };
  const [r, g, b] = rgb;
  return {
    border: `rgba(${r}, ${g}, ${b}, ${borderOpacity})`,
    bg:     `rgba(${r}, ${g}, ${b}, ${bgOpacity})`,
  };
}

// Emit a single [data-callout] rule block.
function ruleBlock(id, color, borderOpacity, bgOpacity) {
  const { border, bg } = derivedVars(color, borderOpacity, bgOpacity);
  return [
    `.callout[data-callout='${id}'] {`,
    `  --callout-color:  ${color};`,
    `  --callout-border: ${border};`,
    `  --callout-bg:     ${bg};`,
    `}`,
  ].join('\n');
}

// Classify a condition. Returns 'light', 'dark', or 'both'.
// Unconditional changes, `theme` conditions, and compound (`and`/`or`) conditions
// all apply to both color schemes — we flatten them rather than dropping them,
// warning so the flattening is visible in build output.
export function classifyCondition(condition) {
  if (condition == null) return 'both';
  if ('colorScheme' in condition) return condition.colorScheme; // 'light' | 'dark'
  if ('theme' in condition) {
    console.warn(`generate-callout-css: 'theme' condition is not scheme-specific — flattening to 'both' color schemes.`);
    return 'both';
  }
  if ('and' in condition || 'or' in condition) {
    console.warn(`generate-callout-css: compound ('and'/'or') condition flattened to 'both' color schemes.`);
    return 'both';
  }
  console.warn(`generate-callout-css: unrecognized condition shape flattened to 'both' color schemes.`, condition);
  return 'both';
}

function main() {
if (!existsSync(GENERATED_DIR)) mkdirSync(GENERATED_DIR, { recursive: true });

if (!existsSync(DATA_PATH)) {
  writeFileSync(CSS_PATH,  HEADER + '/* callout-manager data.json not found */\n');
  writeFileSync(META_PATH, '{}\n');
  return;
}

const data = JSON.parse(readFileSync(DATA_PATH, 'utf-8'));
const customIds   = data?.callouts?.custom   ?? [];
const settingsMap = data?.callouts?.settings ?? {};

if (customIds.length === 0) {
  writeFileSync(CSS_PATH,  HEADER + '/* No custom callouts defined yet */\n');
  writeFileSync(META_PATH, '{}\n');
  console.log('generate-callout-css: no custom callouts.');
  return;
}

const lightRules = [];
const darkRules  = [];
const metadata   = {}; // id → { icon, iconType, title } for remark plugin

for (const id of customIds) {
  const settings = settingsMap[id] ?? [];

  let lightColor  = null;
  let darkColor   = null;
  let bothColor   = null;
  let customStyles = null;
  let iconRaw = null;

  for (const setting of settings) {
    const cls = classifyCondition(setting.condition);

    const raw   = setting.changes?.color;
    const color = raw ? toColor(raw) : null;
    const cs    = setting.changes?.customStyles ?? null;
    const ic    = setting.changes?.icon ?? null;

    if (cls === 'both') {
      if (color) bothColor = color;
      if (cs)    customStyles = cs;
      if (ic && !iconRaw) iconRaw = ic; // first unconditional icon wins
    }
    if (cls === 'light') { if (color) lightColor = color; }
    if (cls === 'dark')  { if (color) darkColor  = color; }
  }

  const resolvedLight = lightColor ?? bothColor;
  const resolvedDark  = darkColor  ?? bothColor;

  // CSS
  if (resolvedLight) {
    let block = ruleBlock(id, resolvedLight, 0.22, 0.07);
    if (customStyles) {
      block = block.replace(/\}$/, `  ${customStyles.trim().replace(/\n/g, '\n  ')}\n}`);
    }
    lightRules.push(`/* ${id} */\n${block}`);
  }

  if (resolvedDark) {
    let block = ruleBlock(id, resolvedDark, 0.28, 0.10);
    if (customStyles) {
      block = block.replace(/\}$/, `  ${customStyles.trim().replace(/\n/g, '\n  ')}\n}`);
    }
    darkRules.push(`.dark .callout[data-callout='${id}'] {\n${
      block.split('\n').slice(1, -1).join('\n')
    }\n}`);
  }

  // Metadata for remark plugin
  const iconInfo = normalizeIcon(iconRaw);
  metadata[id] = {
    icon:      iconInfo?.name ?? 'info',
    iconType:  iconInfo?.type ?? 'lucide',
    title:     idToTitle(id),
  };
}

// Write CSS
let cssOutput = HEADER;
if (lightRules.length) {
  cssOutput += '/* ── Light mode ─────────────────────────────────────────────────────────── */\n\n';
  cssOutput += lightRules.join('\n\n') + '\n\n';
}
if (darkRules.length) {
  cssOutput += '/* ── Dark mode ──────────────────────────────────────────────────────────── */\n\n';
  cssOutput += darkRules.join('\n\n') + '\n';
}
writeFileSync(CSS_PATH, cssOutput);

// Write metadata JSON
writeFileSync(META_PATH, JSON.stringify(metadata, null, 2) + '\n');

console.log(`generate-callout-css: ${customIds.length} custom callout(s) → callouts-custom.css + callouts-custom.json`);
}

// Only run generation when this file is executed directly (e.g. `node scripts/generate-callout-css.js`),
// not when imported (e.g. by tests importing the pure helpers above) — importing must be side-effect-free.
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main();
}
